# Variables are containers for storing data values

# Examples of variables
p = 'Parm'
j = "Jeet"
b = 1995
c = 19.95
d = True

# Printing the variables
print(p)
print(j)
print(b)
print(c)
print(d)

# Printing the type of variables
print(type(p))
print(type(j))
print(type(b))
print(type(c))
print(type(d))
